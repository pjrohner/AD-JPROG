package de.oio.java.oo;

public class Auto {
	
	String hersteller;
	
	String typ;
	
	double breite;
	
	int raeder;
	
	String getHersteller() {
		return hersteller;
	}
	
	String getTyp() {
		return typ;
	}
	
	double getBreite() {
		return breite;
	}
	
	int getRaeder() {
		return raeder;
	}
	
	void setBreite(double neueBreite) {
		breite = neueBreite;
	}
	
}